Random.seed!(1)
yhat = rand(Float32, 10,10)
y    = rand(Bool, 10,10)

@test ml.IoUScore(yhat, y) == 0.32f0 || error("IoUScore failed")
@test ml.AccScore(yhat, y) == 0.49f0 || error("AccScore failed")
@test ml.AccuracyScore(yhat, y) == 0.49f0 || error("AccuracyScore failed")
@test ml.F1Score(yhat, y) == 0.4848485f0 || error("F1Score failed")
@test ml.PrecisionScore(yhat, y) == 0.5f0 || error("PrecisionScore failed")
@test ml.PPVScore(yhat, y) == 0.5f0 || error("PPVScore failed")
@test ml.RecallScore(yhat, y) == 0.47058824f0 || error("RecallScore failed")
@test ml.TPRScore(yhat, y) == 0.47058824f0 || error("TPRScore failed")
@test ml.FPRScore(yhat, y) == 0.48979592f0 || error("FPRScore failed")
@test ml.TNRScore(yhat, y) == 0.5102041f0 || error("TNRScore failed")
